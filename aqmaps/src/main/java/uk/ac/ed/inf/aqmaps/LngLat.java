package uk.ac.ed.inf.aqmaps;

public class LngLat {
	
	private double lng;
	private double lat;

	// Getters and Setters
	public double getLng() {
		return this.lng;
	}
	
	public void setLocation(Double lng) {
		this.lng = lng;
	}
	
	public double getLat() {
		return this.lat;
	}
	
	public void setLat(Double lat) {
		this.lat = lat;
	}
}
